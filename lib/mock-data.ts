export interface LandlordProfile {
  id: string;
  name: string;
  phone: string;
}

/** Returns basic public landlord info derived from their live listings. */
export function getLandlordProfile(landlordId: string): LandlordProfile | undefined {
  const listing = MOCK_LISTINGS.find((l) => l.landlord_id === landlordId);
  if (!listing) return undefined;
  return { id: listing.landlord_id, name: listing.landlord_name, phone: listing.landlord_phone };
}

/** Returns all landlord IDs that have at least one live listing. */
export function getAllLandlordIds(): string[] {
  const ids = new Set(MOCK_LISTINGS.filter((l) => l.status === "live").map((l) => l.landlord_id));
  return Array.from(ids);
}

